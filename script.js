const customAlert = document.getElementById('custom-alert');
const alertMessage = document.getElementById('alert-message');
const closeButton = document.querySelector('.custom-alert .close-button');
const alertOkButton = document.getElementById('alert-ok-button');

function showCustomAlert(messageKey, isSuccess = true) {
    alertMessage.textContent = translations[currentLanguage][messageKey];
    if (isSuccess) {
        customAlert.style.backgroundColor = 'rgba(0,123,255,0.4)'; // Blue for success
    } else {
        customAlert.style.backgroundColor = 'rgba(255,0,0,0.4)'; // Red for error
    }
    customAlert.style.display = 'flex'; // Show the alert
}

function hideCustomAlert() {
    customAlert.style.display = 'none'; // Hide the alert
    customAlert.style.backgroundColor = 'rgba(0,0,0,0.4)'; // Reset background for next time
}

// إغلاق التنبيه عند النقر على X أو OK
if (closeButton) closeButton.addEventListener('click', hideCustomAlert);
if (alertOkButton) alertOkButton.addEventListener('click', hideCustomAlert);

// إغلاق التنبيه عند النقر خارج الصندوق
window.addEventListener('click', function(event) {
    if (event.target === customAlert) {
        hideCustomAlert();
    }
});


// دالة لتطبيق الترجمة على جميع العناصر
function applyTranslation(lang) {
    document.querySelectorAll('[data-key]').forEach(element => {
        const key = element.getAttribute('data-key');
        if (translations[lang] && translations[lang][key]) {
            if (element.placeholder !== undefined) {
                element.placeholder = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });

    const body = document.body;
    body.classList.remove('lang-ar', 'lang-en');
    body.classList.add(`lang-${lang}`);

    localStorage.setItem('preferredLang', lang);
    currentLanguage = lang;

    // هذا هو الجزء الذي يتحكم في إظهار وإخفاء الأزرار
    const langArBtn = document.getElementById('lang-ar');
    const langEnBtn = document.getElementById('lang-en');

    if (langArBtn && langEnBtn) {
        // المنطق النهائي والصحيح لإظهار زر واحد فقط (أزلنا قواعد التشخيص المؤقتة)
        if (lang === 'ar') {
            langArBtn.classList.add('hidden');       // إخفاء زر العربية (لأنها اللغة الحالية)
            langEnBtn.classList.remove('hidden');    // إظهار زر الإنجليزية (للتحويل إليها)
        } else { // lang === 'en'
            langEnBtn.classList.add('hidden');       // إخفاء زر الإنجليزية (لأنها اللغة الحالية)
            langArBtn.classList.remove('hidden');    // إظهار زر العربية (للتحويل إليها)
        }
    }
    // بعد تغيير اللغة، أعد جلب التوصيات لتحديث أي نصوص مترجمة داخلها (مثل "مجهول")
    fetchRecommendations();
}

// دالة لعرض التوصيات
async function fetchRecommendations() {
    const recommendationsContainer = document.querySelector('.recommendations-container');
    recommendationsContainer.innerHTML = ''; // Clear existing recommendations

    try {
        const snapshot = await db.collection('recommendations').orderBy('timestamp', 'desc').get();
        if (snapshot.empty) {
            console.log("No recommendations found in Firestore.");
        }
        snapshot.forEach(doc => {
            const data = doc.data();
            
            // استخدام data.titleOrg أولاً، ثم data.title، ثم القيمة الافتراضية المترجمة
            const recommenderTitleText = data.titleOrg || data.title || translations[currentLanguage].anonymousPlaceholder;

            const card = document.createElement('div');
            card.classList.add('recommendation-card');
            
            card.innerHTML = `
                <p>${data.text}</p>
                <h4>${data.name || translations[currentLanguage].anonymousPlaceholder}</h4>
                <p class="recommender-title">${recommenderTitleText}</p>
            `;
            
            recommendationsContainer.appendChild(card);
        });
    } catch (error) {
        console.error("Error fetching recommendations: ", error);
        showCustomAlert('alertFailedLoad', false);
    }
}

// عند تحميل محتوى DOM بالكامل
document.addEventListener('DOMContentLoaded', function() {
    // استعادة اللغة المفضلة من localStorage عند التحميل
    const savedLang = localStorage.getItem('preferredLang');
    if (savedLang) {
        currentLanguage = savedLang;
    }

    // تطبيق الترجمة فوراً عند تحميل الصفحة
    applyTranslation(currentLanguage);

    // إضافة مستمعي الأحداث لأزرار اللغة
    const langArBtn = document.getElementById('lang-ar');
    const langEnBtn = document.getElementById('lang-en');

    if (langArBtn && langEnBtn) {
        langArBtn.addEventListener('click', () => applyTranslation('ar'));
        langEnBtn.addEventListener('click', () => applyTranslation('en'));
    }

    // التعامل مع نموذج التوصيات
    const recommendationForm = document.getElementById('recommendationForm');
    if (recommendationForm) {
        recommendationForm.addEventListener('submit', async function(e) {
            e.preventDefault();

            const name = document.getElementById('recommender-name').value.trim();
            const titleOrg = document.getElementById('recommender-title-org').value.trim();
            const text = document.getElementById('recommendation-text').value.trim();

            if (!name || !text) {
                showCustomAlert('alertFillForm', false);
                return;
            }

            try {
                await db.collection('recommendations').add({
                    name: name,
                    titleOrg: titleOrg, // التأكد من إرسالها بـ titleOrg
                    text: text,
                    timestamp: firebase.firestore.FieldValue.serverTimestamp()
                });

                showCustomAlert('alertSuccessMessage', true);
                recommendationForm.reset(); // Clear the form
                fetchRecommendations(); // Reload recommendations to show the new one
            } catch (error) {
                console.error("Error adding recommendation: ", error);
                showCustomAlert('alertErrorMessage', false);
            }
        });
    }
});
